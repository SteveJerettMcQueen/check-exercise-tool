CREATE TABLE Chapter(
    chapterId INT NOT NULL GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    chapterName VARCHAR(255) NOT NULL,
    PRIMARY KEY(chapterId)
);

CREATE TABLE Exercise(
    exerId INT NOT NULL GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    chapterId INT,
    exerName VARCHAR(255) NOT NULL,
    exerDescr VARCHAR(255),
    programCode VARCHAR(5000),
    inputParams VARCHAR(2500),
    correctOutput VARCHAR(5000) NOT NULL,
    PRIMARY KEY(exerId),
    FOREIGN KEY(chapterId) REFERENCES Chapter(chapterId) 
    ON DELETE CASCADE 
    ON UPDATE RESTRICT
);